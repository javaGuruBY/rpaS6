package by.jrr.start;


import java.util.logging.Logger;

public class MathOperationsDemo {
    public static void main(String[] args) {
        int a = 25;
        int b = 5;

        System.out.println(a + " + " + b + " = " + (a+b));
        System.out.println(a + " - " + b + " = " + (a-b));
        System.out.println(a + " * " + b + " = " + (a*b));
        System.out.println(a + " / " + b + " = " + (a/b));

    }
}
