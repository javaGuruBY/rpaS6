public class MathOperationsDemo {
    public static void main(String[] args) {

        short m = 15;
        short n = 3;
        System.out.println("m + n = " + (15 + 3)); // 1
        System.out.println("m - n = " + (15 - 3)); // 1
        System.out.println("m * n = " + (15 * 3)); // 1
        System.out.println("m / n = " + (15 /3)); // 1

    }
}
