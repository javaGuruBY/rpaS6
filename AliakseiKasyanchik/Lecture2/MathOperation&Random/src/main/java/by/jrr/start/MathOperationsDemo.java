package by.jrr.start;

public class MathOperationsDemo {
    public static void main (String args[] ) {
        System.out.println("Welcome to the 'Math Operations'!");
        int a = 100;
        int b = 45;
        System.out.println("1) Addition '+':");
        System.out.print("a = " + a + ", b = " + b);

        System.out.println();
        System.out.print("a + b = " + a + " + " + b + " = " + (a + b));

        System.out.println();
        System.out.println();
        int c = 53;
        int d = 17;
        System.out.println("2) Subtraction '-':");
        System.out.print("c = " + c + ", d = " + d);

        System.out.println();
        System.out.print("c - d = " + c + " - " + d + " = " + (c - d));

        System.out.println();
        System.out.println();
        double e = 25;
        double f = 3;
        System.out.println("3) Division '/':");
        System.out.print("e = " + e + ", f = " + f);

        System.out.println();
        System.out.print("e / f = " + e + " / " + f + " = " + (e / f));

        System.out.println();
        System.out.println();
        int g = 9;
        int h = 3;
        System.out.println("4) Multiplication '*':");
        System.out.print("g = " + g + ", h = " + h);

        System.out.println();
        System.out.print("g * h = " + g + " * " + h + " = " + (g * h));
    }
    }
